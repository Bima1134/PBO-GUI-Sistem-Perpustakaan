    private void jButtonBukuPinjamActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonBukuPinjamActionPerformed
        // TODO add your handling code here:
        int selectedIndex = jListHasilCariBuku.getSelectedIndex();
        if (selectedIndex != -1) {
            String selectedJudul = dlm.getElementAt(selectedIndex);
            Buku selectedBuku = null;

            // Cari objek Buku berdasarkan judul yang dipilih
            for (Buku buku : items) {
                if (buku.getJudul().equals(selectedJudul)) {
                    selectedBuku = buku;
                    break;
                }
            }

            if (selectedBuku != null && selectedBuku.getStok() > 0) {
                // Panggil fungsi updatePinjamBuku
                perpusService.updatePinjamBuku(selectedBuku);

                // Konfirmasi peminjaman
                jLabelKeteranganBuku.setText("Buku berhasil dipinjam!");
            } else {
                // Tampilkan pesan stok habis
                jLabelKeteranganBuku.setText("Stok buku habis!");
            }
        } else {
            // Tampilkan pesan jika tidak ada buku yang dipilih
            jLabelKeteranganBuku.setText("Silakan pilih buku yang ingin dipinjam!");
        }
    }//GEN-LAST:event_jButtonBukuPinjamActionPerformed

    private void jButtonCariBukuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonCariBukuActionPerformed
        // Ambil inputan dari pengguna
        String judulInput = jTextAreaJudulBukuCariBuku.getText().trim();
        String penulisInput = jTextAreaPenulisCariBuku.getText().trim();
        String tahunTerbitInput = jTextAreaTahunTerbitCariBuku.getText().trim();

        // Panggil metode cariBuku dari PerpusService
        List<Buku> hasilPencarian = perpusService.cariBuku(judulInput, penulisInput, tahunTerbitInput);

        // Tampilkan hasil pencarian
        tampilkanHasilPencarian(hasilPencarian);

        // Set label keterangan berdasarkan hasil pencarian
        if (!hasilPencarian.isEmpty()) {
            jLabelKeteranganBuku.setText("Buku Ketemu");
        } else {
            jLabelKeteranganBuku.setText("Buku tidak Ditemukan");
        }
    }//GEN-LAST:event_jButtonCariBukuActionPerformed

