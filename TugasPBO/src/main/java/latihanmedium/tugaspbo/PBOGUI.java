    private void tampilkanHasilPencarian(List<Buku> hasilPencarian) {
        dlm.clear();

        if (hasilPencarian.isEmpty()) {
            dlm.addElement("Tidak ada Hasil Ditemukan");
        } 
        else {
            for (Buku buku : hasilPencarian) {
                dlm.addElement(buku.getJudul());
                }
            }
    }

